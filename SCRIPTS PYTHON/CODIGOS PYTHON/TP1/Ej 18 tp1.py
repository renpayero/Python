s = 'Esto es una prueba'

s[0] #Mostrara el caracter 0 de la cadena, N

len(s) #Mostrara la longitud de la cadena

s[::4] #mostrara los caracteres saltando de 4 en 4

s[-2] #Mostrara segundo caracter empezando de derecha a izquierda
        #IMPORTANTE cuando empieza de derecha a izquierda el primer caracter es -1 y no 0

s[::-1] #Mostrara la cadena al reves, esto se debe a que cuando usamos numeros negativos la cadena derecha a izq
        #y al ingresar -1 va a saltar de 1 en 1 dando como resultado la cadena al reves

s[4:10] #Mostrara los caracteres de la cadena entre las posiciones 4 y 10

