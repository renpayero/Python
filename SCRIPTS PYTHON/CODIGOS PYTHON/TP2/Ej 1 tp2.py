n1=input("Ingrese un numero entero")    #Toma a los numeros como strings
n2=input("Ingrese otro numero entero")

n1=float(n1) #Convierte los numeros ingresados a formato float
n2=float(n2) 
#Suma
print("La suma de los numeros es:", n1+n2)
#Resta
print("La resta de los numeros es:", n1-n2)
#Multiplicacion
print("La suma de los numeros es:", n1*n2)
#Cociente de la division
print("El cociente de la division de los numeros es:", n1/n2)
#Resto de la division
print("El resto de la division de los numeros es:", n1%n2)
#La division entera
print("La division entera de los numeros es:", n1//n2)
#El valor absoluto de ambos
print("El valor absoluto del primer numero es:", abs(n1),"\nEl valor absoluto del segundo numero es:", abs(n2))
#NOTA: El comando abs() da como resultado el valor absoluto de un numero







