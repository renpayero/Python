cadena=input("Ingrese una cadena")
vocales="Las vocales son"

for x in cadena:
    if x=="a":
        vocales=vocales+" "+x
    elif x=="e":
        vocales=vocales+" "+x
    elif x=="i":
        vocales=vocales+" "+x
    elif x=="o":
        vocales=vocales+" "+x
    elif x=="u":
        vocales=vocales+" "+x
print(vocales)

        
        
